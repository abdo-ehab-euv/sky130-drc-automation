# ---------------------------------------------------------------------------
# XML parsing helpers
# ---------------------------------------------------------------------------

def _text(elem, tag, default=""):
    """Safely return the text of a direct child tag of `elem`."""
    if elem is None:
        return default
    child = elem.find(tag)
    if child is None or child.text is None:
        return default
    return child.text.strip()


def extract_coords(value_text):
    """
    KLayout writes shape values as strings like:
        polygon: (x1,y1;x2,y2;...)
        box: (x1,y1;x2,y2)
        edge: (x1,y1;x2,y2)

    Pull out the first (x,y) pair. If parsing fails, return ("N/A", "N/A").
    """
    if not value_text:
        return ("N/A", "N/A")
    try:
        start = value_text.find("(")
        if start == -1:
            return ("N/A", "N/A")
        rest = value_text[start + 1:]
        # First point ends at ';' (next vertex) or ')' (end of shape).
        ends = [i for i in (rest.find(";"), rest.find(")")) if i != -1]
        if not ends:
            return ("N/A", "N/A")
        pair = rest[:min(ends)]
        if "," not in pair:
            return ("N/A", "N/A")
        x, y = pair.split(",", 1)
        return (x.strip(), y.strip())
    except Exception:
        return ("N/A", "N/A")


def parse_report(xml_path):
    """
    Parse a KLayout DRC XML file. Returns (categories, items) where:

      categories : dict {rule_name: description}
      items      : list of dicts {rule, x, y, description}

    Defensive: tolerates missing tags, empty files, and minor format drift.
    """
    categories = {}
    items = []

    if not os.path.isfile(xml_path):
        print(f"ERROR: Input XML not found: {xml_path}", file=sys.stderr)
        return categories, items

    try:
        tree = ET.parse(xml_path)
    except ET.ParseError as e:
        print(f"ERROR: Could not parse XML ({xml_path}): {e}", file=sys.stderr)
        return categories, items

    root = tree.getroot()

    # --- categories ---
    # KLayout places these under <categories><category>...</category></categories>
    for cat in root.iter("category"):
        name = _text(cat, "name")
        desc = _text(cat, "description", default=name)
        if name and name not in categories:
            categories[name] = desc

    # --- items ---
    # Each <item> has a <category> child (or sometimes attribute) and a
    # <values><value>...</value></values> subtree describing the geometry.
    # KLayout sometimes wraps the category text in single quotes in the item
    # ('LI.W.1') while the category definition is unquoted (LI.W.1), so we
    # strip surrounding quotes/whitespace to make the two match up.
    for item in root.iter("item"):
        rule = _text(item, "category") or item.get("category", "") or "UNKNOWN"
        rule = rule.strip().strip("'").strip('"')

        # Find the first <value> anywhere under this item.
        value_elem = item.find(".//value")
        value_text = value_elem.text if (value_elem is not None
                                         and value_elem.text) else ""
        x, y = extract_coords(value_text)

        items.append({
            "rule":        rule,
            "x":           x,
            "y":           y,
            "description": categories.get(rule, rule),
        })

    return categories, items


# ---------------------------------------------------------------------------
# Summarisation
# ---------------------------------------------------------------------------

def build_summary(categories, items):
    """
    Build a per-rule summary: rule, description, count, status.

    For each rule we scan the items list once to count matches.
    # TODO: this can be optimized later
    """
    rule_names = list(categories.keys())
    if not rule_names:
        # Fall back to rules seen in the items themselves.
        rule_names = sorted({it["rule"] for it in items})

    summary = []
    for rule in rule_names:
        # Walk the full items list per rule (clear but O(rules * items)).
        matching = [it for it in items if it["rule"] == rule]
        count = len(matching)
        summary.append({
            "rule":        rule,
            "description": categories.get(rule, rule),
            "count":       count,
            "status":      "FAIL" if count > 0 else "PASS",
        })
    return summary


# ---------------------------------------------------------------------------
# Outputs: chart, HTML, terminal
# ---------------------------------------------------------------------------

def make_chart(summary, chart_path):
    """Render a bar chart of violation counts per rule."""
    rules  = [s["rule"]  for s in summary]
    counts = [s["count"] for s in summary]

    fig, ax = plt.subplots(figsize=(8, 4.5))
    if not rules:
        ax.text(0.5, 0.5, "No DRC categories in report",
                ha="center", va="center", transform=ax.transAxes)
        ax.set_axis_off()
    else:
        colors = ["#cc3333" if c > 0 else "#339966" for c in counts]
        ax.bar(rules, counts, color=colors)
        ax.set_title("DRC Violation Count by Rule")
        ax.set_ylabel("Violations")
        for label in ax.get_xticklabels():
            label.set_rotation(20)
            label.set_ha("right")
        # Whole-number y-axis only (violation counts are integers).
        ax.yaxis.get_major_locator().set_params(integer=True)

    fig.tight_layout()
    fig.savefig(chart_path, dpi=150)
    plt.close(fig)


def write_html(summary, items, chart_path, html_path, source_xml):
    total   = sum(s["count"] for s in summary)
    overall = "PASS" if total == 0 else "FAIL"

    # Reference the chart by its basename so the HTML stays portable next
    # to the image file.
    chart_ref = os.path.basename(chart_path)

    rendered = HTML_TEMPLATE.render(
        summary   = summary,
        items     = items,
        total     = total,
        overall   = overall,
        chart     = chart_ref,
        source_xml= os.path.basename(source_xml),
    )
    with open(html_path, "w", encoding="utf-8") as f:
        f.write(rendered)


def print_terminal_summary(summary, total, html_path, chart_path):
    print()
    print("=== DRC Summary ===")
    if summary:
        rows = [[s["rule"], s["description"], s["count"], s["status"]]
                for s in summary]
        print(tabulate(rows,
                       headers=["Rule", "Description", "Count", "Status"],
                       tablefmt="github"))
    else:
        print("  (no rules found in report)")
    print()

    # A pandas DataFrame of the rule-level counts, useful for quick stats
    # and kept around so pandas is a genuinely-used dependency.
    df = pd.DataFrame(summary)
    if not df.empty:
        fail_count = int((df["status"] == "FAIL").sum())
        pass_count = int((df["status"] == "PASS").sum())
        print(f"Rules passing : {pass_count}")
        print(f"Rules failing : {fail_count}")
    print(f"Total violations: {total}")
    print(f"HTML report     : {html_path}")
    print(f"Chart image     : {chart_path}")


# ---------------------------------------------------------------------------
# Entrypoint
# ---------------------------------------------------------------------------

def parse_args():
    p = argparse.ArgumentParser(
        description="Parse a KLayout DRC XML report and build an HTML dashboard.")
    p.add_argument("--input", default="results.xml",
                   help="KLayout DRC XML file (default: results.xml)")
    p.add_argument("--html",  default="drc_report.html",
                   help="Output HTML report path (default: drc_report.html)")
    p.add_argument("--chart", default="violations.png",
                   help="Output chart image path (default: violations.png)")
    return p.parse_args()


def main():
    args = parse_args()

    categories, items = parse_report(args.input)

    if not categories and not items:
        print(f"WARNING: No categories or items found in {args.input}. "
              "The report will still be generated but will be empty.",
              file=sys.stderr)

    summary = build_summary(categories, items)
    total   = sum(s["count"] for s in summary)

    make_chart(summary, args.chart)
    write_html(summary, items, args.chart, args.html, args.input)

    print_terminal_summary(summary, total, args.html, args.chart)


if __name__ == "__main__":
    main()
